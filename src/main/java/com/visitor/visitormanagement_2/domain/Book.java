package com.visitor.visitormanagement_2.domain;

import javax.persistence.*;

@Entity
@Table(name = "book")
public class Book {
    @Id
    private int id;
    @Column(name="Name")
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Book{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
